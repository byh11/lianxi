package zuoye;

public class jsq {
    private int count;

    public void increment(){
        count++;
    }
    public void decrement(){
        count--;
    }
    public void reset(){
        count=0;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
